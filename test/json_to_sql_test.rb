  def test_schema_driven_ddl_with_pii_comments_and_meta_table
    Dir.mktmpdir do |dir|
      schema_dir = File.join(dir, "schema")
      write(File.join(schema_dir, "users.schema.json"), USERS_SCHEMA)
      input = write(File.join(dir, "users.json"), SCHEMA_USERS_DATA)
      output = File.join(dir, "users.sql")
      stats, sql = convert(input, output, schema_dir: schema_dir)

      # Schema-declared types, not inferred ones.
      assert_includes sql, "`id` TEXT NOT NULL"
      assert_includes sql, "`login` TEXT NOT NULL COMMENT 'x-pii'"
      assert_includes sql, "`email` TEXT COMMENT 'x-pii'"
      assert_includes sql, "`banned` TINYINT(1) NOT NULL"
      assert_includes sql, "`score` DOUBLE,"
      assert_includes sql, "`registration_time` DATETIME NOT NULL"
      assert_includes sql, "`status` VARCHAR(32) NOT NULL"
      assert_includes sql, "`plus_one_json` LONGTEXT,"
      assert_includes sql, "`avatar_url` VARCHAR(100) NOT NULL"
      # TEXT id columns cannot carry a plain KEY in MySQL.
      refute_includes sql, "KEY `idx_id`"

      assert_includes sql, "CREATE TABLE `users_badges`"
      assert_includes sql, "`title` TEXT,"

      # Row emission: schema-typed values, unexpected field dropped.
      assert_includes sql, "(1, 'user:1', 'alice', NULL, 0, 1, '2024-01-02 03:04:05', 'PENDING', '{\"who\":\"knows\"}', 'https://example.com/a.png')"
      assert_includes sql, "(1, 1, 'user:1', 0, 'badge:1', 'First')"
      refute_includes sql, "dropped"
      assert_equal 1, stats[:dropped_values]

      # PII manifest table.
      assert_includes sql, "CREATE TABLE `_json_meta`"
      assert_includes sql, "('users', 'login', 'login', 1, 'Username', 'users.schema.json')"
      assert_includes sql, "('users', 'email', 'email', 1, NULL, 'users.schema.json')"
    end
  end

