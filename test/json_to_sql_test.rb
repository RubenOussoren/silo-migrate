  def test_schema_dir_without_matching_schema_falls_back_to_inference
    Dir.mktmpdir do |dir|
      schema_dir = File.join(dir, "schema")
      FileUtils.mkdir_p(schema_dir)
      input = write(File.join(dir, "users.json"), USERS_JSON)
      output = File.join(dir, "users.sql")

      converter = SiloMigrate::JSONToSQLConverter.new(schema_dir: schema_dir, verbose: true)
      stdout, = capture_io { converter.convert(Pathname(input), Pathname(output)) }

      assert_includes stdout, "[WARN] No JSON Schema found for users.json"
      sql = File.read(output)
      assert_includes sql, "CREATE TABLE `users`"
      refute_includes sql, "CREATE TABLE `_json_meta`"
    end
  end

