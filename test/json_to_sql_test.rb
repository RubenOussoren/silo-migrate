  def test_type_widening_to_string_and_text_tiers
    rows = [
      { "mixed" => 1, "big" => "x" * 300, "absent" => "here" },
      { "mixed" => "two", "big" => "y" }
    ]
    json = Oj.dump({ "object_name" => "things", "total_records" => 2, "data" => rows }, mode: :compat)

    Dir.mktmpdir do |dir|
      input = write(File.join(dir, "things.json"), json)
      output = File.join(dir, "things.sql")
      _, sql = convert(input, output)

      assert_includes sql, "`mixed` VARCHAR(255) NOT NULL"
      assert_includes sql, "`big` TEXT NOT NULL"
      # Field missing from one record must be nullable.
      assert_includes sql, "`absent` VARCHAR(255),"
      # Integer in a string-widened column is emitted as its quoted string form.
      assert_includes sql, "(1, '1', '#{'x' * 300}', 'here')"
      assert_includes sql, "(2, 'two', 'y', NULL)"
    end
  end

