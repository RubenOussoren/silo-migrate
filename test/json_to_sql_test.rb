    {
      "object_name": "users",
      "total_records": 2,
      "data": [
        {
          "id": "user:1",
          "uid": 1,
          "score": 1,
          "login": "alice",
          "banned": false,
          "email": null,
          "createdAt": "2024-01-02T03:04:05Z",
          "avatar": {"url": "https://example.com/a.png"},
          "replies": {"totalCount": 3},
          "tags": {"edges": [
            {"node": {"id": "tag:1", "text": "one"}},
            {"node": {"id": "tag:2", "text": "two"}}
          ]},
          "ssoFields": ["terms", "privacy"]
        },
        {
          "id": "user:2",
          "uid": 2,
          "score": 2.5,
          "login": "bob",
          "banned": true,
          "email": "bob@example.com",
          "createdAt": "2024-02-03T04:05:06+02:00",
          "avatar": {"url": "https://example.com/b.png"},
          "replies": {"totalCount": 0},
          "tags": {"edges": []},
          "ssoFields": []
        }
      ]
    }
  JSON

  def convert(input, output, **options)
    converter = SiloMigrate::JSONToSQLConverter.new(verbose: false, **options)
    stats = converter.convert(Pathname(input), Pathname(output))
    [stats, File.read(output)]
  end

  def test_converts_envelope_file_with_flattening_and_child_tables
    Dir.mktmpdir do |dir|
      input = write(File.join(dir, "users.json"), USERS_JSON)
      output = File.join(dir, "users.sql")
      stats, sql = convert(input, output)

      assert_includes sql, "-- Generated by json-to-sql converter"
      assert_includes sql, "SET NAMES utf8mb4;"
      assert_includes sql, "SET FOREIGN_KEY_CHECKS = 0;"
      assert_match(/SET FOREIGN_KEY_CHECKS = 1;\nSET UNIQUE_CHECKS = 1;\n\z/, sql)

      assert_includes sql, "CREATE TABLE `users`"
      assert_includes sql, "`_sid` BIGINT NOT NULL"
      assert_includes sql, "PRIMARY KEY (`_sid`)"
      assert_includes sql, "`id` VARCHAR(255) NOT NULL"
      assert_includes sql, "KEY `idx_id` (`id`)"
      assert_includes sql, "`uid` BIGINT NOT NULL"
      assert_includes sql, "`score` DOUBLE NOT NULL"
      assert_includes sql, "`banned` TINYINT(1) NOT NULL"
      assert_includes sql, "`email` VARCHAR(255),"
      assert_includes sql, "`created_at` DATETIME NOT NULL"
      assert_includes sql, "`avatar_url` VARCHAR(255) NOT NULL"
      assert_includes sql, "`replies_total_count` BIGINT NOT NULL"

      assert_includes sql, "CREATE TABLE `users_tags`"
      assert_includes sql, "`_parent_sid` BIGINT NOT NULL"
      assert_includes sql, "`_parent_id` VARCHAR(255)"
      assert_includes sql, "`_ordinal` BIGINT NOT NULL"
      assert_includes sql, "KEY `idx_parent_sid` (`_parent_sid`)"
      assert_includes sql, "KEY `idx_parent_id` (`_parent_id`)"

      assert_includes sql, "CREATE TABLE `users_sso_fields`"
      assert_includes sql, "`value` VARCHAR(255) NOT NULL"

      # Root rows: NULL email for alice, UTC-normalized datetime for bob.
      assert_includes sql, "(1, 'user:1', 1, 1, 'alice', 0, NULL, '2024-01-02 03:04:05', 'https://example.com/a.png', 3)"
      assert_includes sql, "(2, 'user:2', 2, 2.5, 'bob', 1, 'bob@example.com', '2024-02-03 02:05:06', 'https://example.com/b.png', 0)"

      # Child rows carry synthetic + natural parent keys and 0-based ordinals.
      assert_includes sql, "(1, 1, 'user:1', 0, 'tag:1', 'one')"
      assert_includes sql, "(2, 1, 'user:1', 1, 'tag:2', 'two')"
      assert_includes sql, "(1, 1, 'user:1', 0, 'terms')"
      assert_includes sql, "(2, 1, 'user:1', 1, 'privacy')"

      assert_equal 3, stats[:tables_processed]
      assert_equal 2, stats[:rows_processed]
      assert_equal 4, stats[:child_rows_processed]
      assert_equal 1, stats[:files_processed]
    end
  end

