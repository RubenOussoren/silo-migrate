  def test_conversion_does_not_slurp_json_with_file_read
    Dir.mktmpdir do |dir|
      input = write(File.join(dir, "users.json"), USERS_JSON)
      output = File.join(dir, "users.sql")
      file_singleton = class << File; self; end
      verbose = $VERBOSE
      $VERBOSE = nil
      file_singleton.alias_method :read_without_json_stream_test, :read
      file_singleton.define_method(:read) do |*args|
        raise "JSON conversion must stream input instead of using File.read" if args.first.to_s.end_with?(".json")

        read_without_json_stream_test(*args)
      end
      $VERBOSE = verbose

      convert(input, output)
    ensure
      if file_singleton&.method_defined?(:read_without_json_stream_test)
        verbose = $VERBOSE
        $VERBOSE = nil
        file_singleton.alias_method :read, :read_without_json_stream_test
        file_singleton.remove_method :read_without_json_stream_test
        $VERBOSE = verbose
      end
    end
  end

  def test_streaming_smoke_with_many_records
    Dir.mktmpdir do |dir|
      input = File.join(dir, "posts.json")
      File.open(input, "w") do |file|
        file.write('{"object_name": "posts", "total_records": 50000, "data": [')
        50_000.times do |index|
          file.write(",") if index.positive?
          file.write(%({"id": "post:#{index}", "n": #{index}, "tags": {"edges": [{"node": {"t": "tag#{index % 7}"}}]}}))
        end
        file.write("]}")
      end
      output = File.join(dir, "posts.sql")
      snapshots = []

      stats = SiloMigrate::JSONToSQLConverter.new(verbose: false).convert(
        Pathname(input),
        Pathname(output),
        progress_callback: proc { |snapshot| snapshots << snapshot },
        progress_interval: 0
      )

      assert_equal 50_000, stats[:rows_processed]
      assert_equal 50_000, stats[:child_rows_processed]
      assert snapshots.any? { |snapshot| snapshot[:event] == :bytes && snapshot[:current_file][:bytes_read].positive? }
      assert snapshots.any? { |snapshot| snapshot[:event] == :rows }
      assert_includes File.read(output, 2000), "CREATE TABLE `posts`"
    end
  end

